import UIKit

extension UIFont {
	static func AvenirNextRegular(size: CGFloat) -> UIFont {
		return .init(name: "AvenirNext-Regular", size: size)!
	}
	static func AvenirNextMedium(size: CGFloat) -> UIFont {
		return .init(name: "AvenirNext-Medium", size: size)!
	}
	static func AvenirNextBold(size: CGFloat) -> UIFont {
		return .init(name: "AvenirNext-Bold", size: size)!
	}
}
